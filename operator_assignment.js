let a;
let b;

// Operator Penugasan Dasar
a = 10; //Penugasan nilai 10 ke a
b = 5; // Penugasan nilai 5 ke b
let c = a; //Penugasan nilai a ke c
console.log("a =", a); // Output: 10
console.log("b =", b); // Output: 5
console.log("c =", c); // Output: 10
//Operator Penugasan Penjumlahan
c += b; // Sama dengan c = c + b
console.log("c =", c); // Output: 15
//Operator Penugasan Pengurangan 
c -= b; // Sama dengan c = c - b
console.log("c =", c); // Output: 10
// Operator Penugasan Perkalian
c *= b; // Sama dengan c = c * b
console.log("c =", c); // Output: 50
//Operator Penugasan Pembagian
c /= b; //Sama dengan c = c / b
console.log("c =", c);// Output: 10
//Operator Penugasan Sisa Bagi
c %= b; //Sama dengan c = c % b
console.log("c =", c); //Output: 0
//Operator Penugasan Eksponen
c **= 2; // Sama dengan c = c ** 2(mengangkat c ke pangkat 2)
console.log("c =", c); // Output: 0